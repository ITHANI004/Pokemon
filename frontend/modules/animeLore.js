export function getAnimeChronicle(poke) {
    const id = poke.pokedex_number;
    if (customAnimeLore[id]) {
        return customAnimeLore[id];
    }

    let era = "Original Series: Indigo League (Kanto)";
    let trainer = "Various Kanto Trainers & Gym Leaders";
    let story = `Appeared during Ash Ketchum's original adventures across the Kanto region as Pokémon Trainers discovered the first 151 species documented by Professor Oak!`;

    if (id >= 152 && id <= 251) {
        era = "Pokémon: The Johto Journeys (Gen 2)";
        trainer = "Johto League Challengers";
        story = `First encountered during Ash, Misty, and Brock's expedition through the Johto region, discovering baby Pokémon and mysterious new evolutions from Professor Elm's research!`;
    } else if (id >= 252 && id <= 386) {
        era = "Pokémon: Advanced Generation (Hoenn)";
        trainer = "Hoenn Coordinators & Trainers";
        story = `Prominently featured during Ash's journey across the Hoenn region alongside May, Max, and Brock, where Pokémon Contests and Double Battles took center stage!`;
    } else if (id >= 387 && id <= 493) {
        era = "Pokémon: Diamond & Pearl (Sinnoh)";
        trainer = "Sinnoh League Competitors";
        story = `Featured throughout the dramatic Sinnoh region series alongside Dawn and Brock, showcasing ancient mythology and fierce galactic rivalries!`;
    } else if (id >= 494 && id <= 649) {
        era = "Pokémon: Black & White (Unova)";
        trainer = "Unova Trainers & Gym Leaders";
        story = `Encountered during Ash's fresh-start journey through the distant Unova region alongside Dragon Master Iris and Pokémon Connoisseur Cilan!`;
    } else if (id >= 650 && id <= 721) {
        era = "Pokémon the Series: XY & XYZ (Kalos)";
        trainer = "Kalos Performers & Mega Trainers";
        story = `Featured in high-octane battles across the Kalos region with Serena, Clemont, and Bonnie, where the secrets of Mega Evolution and Bond Phenomenon unfolded!`;
    } else if (id >= 722 && id <= 809) {
        era = "Pokémon the Series: Sun & Moon (Alola)";
        trainer = "Alola Pokémon School Classmates";
        story = `Encountered in the tropical Kanto-Alola exchange series where Ash attended Pokémon School, learned Z-Moves, and became the first-ever Alola League Champion!`;
    } else if (id >= 810 && id <= 905) {
        era = "Pokémon Journeys: The Series (Galar / World)";
        trainer = "World Coronation Series Challengers";
        story = `Featured during Ash Ketchum and Goh's global research fellowship across every known region, culminating in the Masters Eight World Championship!`;
    } else if (id >= 906) {
        era = "Pokémon Horizons: The Series (Paldea)";
        trainer = "Rising Volt Tacklers & Explorers";
        story = `Featured in the modern Pokémon Horizons series across the Paldea region starring Liko with her magical pendant and Roy with his mysterious ancient Poké Ball!`;
    }

    return {
        debut: era,
        trainer: trainer,
        story: story
    };
}
